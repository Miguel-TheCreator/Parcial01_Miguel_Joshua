def texto_mayusculas(cadena):
    print("Convirtiendo a MAYÚSCULAS...")
    return cadena.upper()
    
#  Joshua Forbes
def texto_minusculas(cadena):
    print("Convirtiendo a minúsculas...")
    return cadena.lower()

# Pruebas
print(texto_mayusculas("Hola Mundo"))
print(texto_minusculas("Hola Mundo"))

print(texto_mayusculas("Hola Miguel"))
print(texto_minusculas("Hola Joshua"))

